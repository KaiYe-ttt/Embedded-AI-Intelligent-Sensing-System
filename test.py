# -------------------------- 第二步：模拟C端Dense计算逻辑（核心：int32累加防溢出，int8裁剪） --------------------------
def dense_infer_c_sim(input_x, w, b, out_dim):
    """
    模拟C端Dense层计算：input_x (in_dim,)int8 × w(in_dim,out_dim)int8 + b(out_dim,)int8
    :param input_x: 输入向量（in_dim,）int8
    :param w: 权重矩阵（in_dim,out_dim）int8
    :param b: 偏置向量（out_dim,）int8
    :param out_dim: 输出维度
    :return: 输出向量（out_dim,）int8（裁剪到-128~127）
    """
    in_dim = input_x.shape[0]
    output = np.zeros(out_dim, dtype=np.int32)  # 用int32累加，模拟C端的int32_t

    # 1. 矩阵乘法：input_x × w → 逐元素乘+累加（C端的for循环实现）
    for i in range(out_dim):
        for j in range(in_dim):
            output[i] += int(input_x[j]) * int(w[j, i])  # 转int防溢出，复刻C端的(int32)a*(int32)b

    # 2. 加偏置
    output = output + b.astype(np.int32)

    # 3. 裁剪到int8范围[-128,127]，模拟C端的__CLAMP
    output = np.clip(output, -128, 127)

    # 4. 转为int8，得到最终输出
    return output.astype(np.int8)


